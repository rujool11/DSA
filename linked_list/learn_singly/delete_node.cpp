// Leetcode 237. Delete node in linked list 
// not as normal node deletion, "delete" by direct node input
// copy value and next from next node, dont actually delete anything (question constraints)


/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    void deleteNode(ListNode* node) {
        node->val = node->next->val;
        node->next = node->next->next;
    }
};